package com.withertech.mine_gui.fabric;

import com.withertech.mine_gui.MineGui;
import com.withertech.mine_gui.impl.client.MineGuiConfig;
import me.shedaniel.autoconfig.AutoConfig;
import me.shedaniel.autoconfig.serializer.GsonConfigSerializer;
import net.fabricmc.api.ModInitializer;

public class MineGuiFabric implements ModInitializer
{
	@Override
	public void onInitialize()
	{
		AutoConfig.register(MineGuiConfig.class, GsonConfigSerializer::new);
		MineGui.init();
	}
}